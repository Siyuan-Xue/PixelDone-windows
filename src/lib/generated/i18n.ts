export type MessageKey = keyof typeof messages.en;
export type Locale = keyof typeof messages;

export function localeFor(language: AppLanguage): Locale {
  if (language === 'ENGLISH') return 'en';
  if (language === 'SIMPLIFIED_CHINESE') return 'zh-Hans';
  if (language === 'ARABIC') return 'ar';
  if (language === 'FRENCH') return 'fr';
  if (language === 'RUSSIAN') return 'ru';
  if (language === 'SPANISH') return 'es';
  const browser = typeof navigator === 'undefined' ? 'en' : navigator.language;
  if (browser.startsWith('zh')) return 'zh-Hans';
  if (browser.startsWith('ar')) return 'ar';
  if (browser.startsWith('fr')) return 'fr';
  if (browser.startsWith('ru')) return 'ru';
  if (browser.startsWith('es')) return 'es';
  return 'en';
}

export function message(locale: Locale, key: MessageKey): string {
  const localized = messages[locale] as Partial<Record<MessageKey, string>>;
  return localized[key] ?? messages.en[key];
}
